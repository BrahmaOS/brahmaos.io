        <h3 id="AccessibleNetworkCommunication"> Accessible Network Communication</h3>
        <p> Whether in the blockchain or the current Internet economy,
            network communication connections are issues that need to be resolved first.
            How to access data on the web without any problems has become a crucial issue in many cases.
        </p>

        <p>
            When we talk about network communications,
            most of the time we are talking about how the terminal connects to a wide area network.
            In the current architecture, the terminal device connects to the meta-network (i.e., the local area network to which it can connect directly).
            Then meta-network connects to different meta-network or the upper network through the router and switch.
            Most of the connections between different meta-networks are operators.We can then talk about meta-network and router.
        </p>

        <p>
            The end nodes in the meta-network can form a peer-to-peer network,
            that is, the nodes in any meta-network are offline and should not cause unreachable communication in the meta-network.
            This is not the core of our concern, as its connectivity problems are not hard to solve.
            The critical problem goes to the router.
        </p>

        <p>
            In current network communications, we must trust and can only trust the router.
            In fact, it not only can completely intercept and spy on the data, protocol,
            and trend of communication between meta-networks but also act on it, such as tampering and denial of service.
        </p>
        <p>

            In currently available network communications,
            operating system end-users face two major problems: privacy exposure, unprotected accessibility.
        </p>

        <p>
            Therefore, in the Brahma OS,
            we expect to build a peer-to-peer OS network to ensure the encryption of the communication data
            and the non- identifiability of communication data through protocol confusion at the data
            transport level, and to build an autonomous connected network based on the configuration of the routing.
        </p>

