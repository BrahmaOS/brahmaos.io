        <h3 id="DecentralizedStorage"> Decentralized Storage</h3>
        <p>
            Today, most of the OS provides users with the cloud storage function.
            Users in different devices only need to log in to the same account to have access to the same data.
            Cloud storage brings users personal convenience. At the same time,
            it can also bring some potential trouble for personal data security.
        </p>
        <p>
            Dropbox loopholes lead to loss of personal data.
            Many nude photos of some celebrities were leaked when the iCloud account was attacked by lots of hackers.
            Incidents like these events have reflected the apparent problem: in the current centralized cloud storage architecture,
            It is only a matter of time before the security problem is revealed, in other words,
            there is a systematic risk that can not be avoided.
        </p>
        <p>
            Decentralized chain storage avoids this kind of problem from the very beginning of the design.
            Taking IPFS as an example, the decentralized storage has no single server that can be attacked or a group of servers that can be traced.
            All data are divided into multiple parts, randomly stored in different nodes of the network.
            The entire network is safe and efficient for storage and transmission by miners in storage networks.
        </p>

        <p>
            When building storage services, Brahma OS uses directly decentralized web services. For now,
            we do not guarantee the use of IPFS. We would compare other potential decentralized storage like Sia, Storj, MaidSafe.
        </p>

