    <!--Role of Network-->
    <br>
    <h3 id="RoleofNetwork">网络角色</h3>
    <p>
        在 Brahma OS的网络层，我们通过分离不同的角色，来确保网络的连通性和相应的自治经济构建。其中，我们会区分出元网络节点和路由网络节点两类。
    </p>

    <br>
    <h3 id="ConstructionofMetaNetwork">元网络的构建</h3>
    <p>
        元网络是一个局域网络的最小单元。
    </p>
    <p>
        在元网络内，不同节点之间是彼此对等，彼此间连通不需要额外的流量成本，所以元网络的构建主要发生在小范围内，甚至是近距离直接通过蓝牙等协议完成设备间互联。
    </p>
    <p>
        Brahma OS之间可以互联构建元网络，并以此为最小单位向外扩散。在元网络内传出数据时，应用层协议本身会做诸多的混淆措施，会对数据做分片、加密等处理，从而确保数据在元网络内的传输不会泄漏数据内容。
    </p>

    <br>
    <!--Routing-->
    <h3 id="Routing">路由</h3>
    <p>
        路由扮演着连接不同元网络及连通到上层广域网络的角色。多个路由本身也可以形成一个元网络的结构。它们为不同的元网络之间提供数据交换的服务。这边可以形成一个无中心的流量市场。
    </p>
    <p>
        Brahma OS中，连通不同元网络的节点作为路由节点，它们为不同元网络之间及元网络与广域网间的互通提供流量交换，因此作为路由节点的设备也会在一定程度上获得网络连通中的经济回报。
    </p>

    <br>
    <!--Application Basis-->
    <h1 id="ApplicationBasis">应用基础</h1>
    <p>
        Brahma OS集成给类去中心化服务的目的，是为之上的应用提供可以基础，让D-App开发者可以不必过多的考虑如何接入去中心化服务，只需要关注在如何构建应用逻辑。
    </p>

    <br>
    <!--Runtime environment-->
    <h3 id="Runtimeenvironment">运行时环境</h3>
    <p>
        Brahma OS运行时环境作为Native D-App的运行沙盒环境。运行时环境需要确保几点：
    </p>
    <ul>
        <li class="li_disc">完全隔离</li>
        {{--bug--}}
        <li class="li_disc">执行效率</li>
        <li class="li_disc">去中心化组件的调用环境</li>
    </ul>

    <p>
        其中，完全的隔离为D-App提供安全的运行空间，这里的安全不单单是指单一App本身的数据不会被侵犯，同时也指与其他App之间运行是彼此隔离。
    </p>

    <p>
        作为Native运行的App，执行效率至关重要，这直接关系到用户体验的好坏。在系统的线程优先级层面，Brahma OS会优先处理与用户交互相关的事务，而网络、IO相关的执行则并行多线程在后台运行。
    </p>

