    <h3 id="D-AppStore">D-App Store的可能性</h3>
    <p>
        Brahma OS除了在技术上带来诸多的突破，还在为未来可能的新生态带来另一个巨大的可能性，D-App Store。
    </p>

    <p>
        D-App Store不同于当前当前我们已知的给类App Store或Google Play，我们预期它不是一个中心化的运营主体，而是一个由用户直接参与运营和筛选的去中心化集市。其上可以构建和尝试很多新的商业模式，比如D-App的收益可能可以直接与评分用户或推荐用户直接关联，不再存在中间的角色剥夺D-App主体的收入。
    </p>

    <p>
        而且，因为用户身份的数据不再是分裂在不同的中心系统中，所以不同的D-App之间可以基于同一个用户身份作出更为人性化的定制，甚至于从D-App Store时这些数据就已经起到作用，而开发者可以通过付费给D-App Store的推荐系统，来寻找到符合其目标群体的用户，这些用户不但会直接收到收益（事实上用户会认为他们什么都没做就收到了代币），而且还会促进D-App的高转化率。
    </p>

